# You can return a range of characters by using the slice syntax.
# Specify the start index and the end index, separated by a colon, to return a part of the string
a="hello every one"

print(a[4:11])  #display all the character from index 0 to index 11
print(a[:10])   #dispaly character from starting to index 10
print(a[2:])    #display all character from index 2 to end 


